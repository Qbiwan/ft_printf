#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*ptr;

	ptr = malloc(size * nmemb);
	if (ptr)
		ptr = ft_memset(ptr, 0, size * nmemb);
	else
		ptr = NULL;
	return (ptr);
}
